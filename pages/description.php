<div><br>Die Terminverwaltung beinhaltet diese <b>Funktionen</b>:</div>
<div>
 - Eintragung eines Termins in die Datenbanktabelle<br>
 - Löschung eines Termins<br>
 - Aktualisierung der Daten eines Termins<br>
 - Kopieren (der Daten) eines Termins<br>
 - Menü-gesteuerte Suche eines Termins<br>
</div>

<div><br>Als <b>Kalendermenüs</b> stehen die folgenden <b>Darstellungen</b> zur Wahl:</div>
<div>
 - Monatsmenü, Tage mit eingetragenen Terminen sind schraffiert<br>
 - Monats-, Wochen-, Tagesblatt mit halbgrafischer Darstellung aller Termine<br>
 - Übersichtsliste aller Termine mit Filterfunktionen<br>
 - tabellarische Liste aller Termine<br>
 - tabellarische Darstellung der Daten eines Termins<br>
</div>

<div><br>Alle <b>Kalendermenüs</b> gestatten das <b>Blättern</b> zum vorherigen und
nachfolgenden Zeitabschnitt, das Monatsmenü zusätzlich zum gleichen Monat
im Vor- oder Folgejahr. Zudem sind sie <b>untereinander verlinkt</b>, sodass man
von einem zum anderen wechseln kann. Die Kalenderfunktionen liefern in Form von
tooltips gesetzliche und christliche Feiertage.</div>